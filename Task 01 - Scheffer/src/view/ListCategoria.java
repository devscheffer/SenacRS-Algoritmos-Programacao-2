    private void carregarTabela() {
        List<?> lista;
        DefaultTableModel model = new DefaultTableModel();
        String[] colunas = {"Id", "Categoria"};
        lista = CategoriaDAO.getCategoria();
            
        model.setColumnIdentifiers(colunas);
        for (Object cat : lista) {
            Object[] linha = {};

            linha = new Object[]{
                    cat.getId()
                    ,cat.getNome() 
                };
            model.addRow( linha );
        }
        
        tableCategoria.setModel( model );
    }

